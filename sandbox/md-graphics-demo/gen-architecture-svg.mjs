// gen-architecture-svg.mjs — emit a committed SVG from code (no deps).
//
// Why this exists: inline <svg> and data: URIs are stripped by GitHub's
// markdown sanitizer, so a vector diagram must be a *committed file* referenced
// by path. This script is the "just code" half of that recipe — the diagram is
// generated, diffable, and regenerable, never hand-drawn in an editor.
//
//   node gen-architecture-svg.mjs > architecture.svg
//
// It draws the AC boot pipeline (boot → BIOS → disk → pieces) the way the real
// runtime is laid out in system/public/aesthetic.computer/.

const W = 720;
const H = 260;

// Citrus-neo-ish palette, dark canvas (mirrors AC's terminal aesthetic).
const C = {
  bg: "#0b0f0a",
  edge: "#3a4a32",
  node: "#11160d",
  stroke: "#9bd64a",
  text: "#d7f0b0",
  muted: "#7f9a5e",
  accent: "#e8ff8a",
};

const boxes = [
  { id: "boot", x: 24, label: "boot.mjs", sub: "entry · telemetry" },
  { id: "bios", x: 196, label: "bios.mjs", sub: "runtime coordinator" },
  { id: "disk", x: 368, label: "disk.mjs", sub: "piece API (~572KB)" },
  { id: "piece", x: 540, label: "pieces", sub: ".mjs · .lisp" },
];
const BW = 156;
const BH = 64;
const BY = 96;

function box(b) {
  const cx = b.x + BW / 2;
  return `
  <g>
    <rect x="${b.x}" y="${BY}" rx="10" ry="10" width="${BW}" height="${BH}"
          fill="${C.node}" stroke="${C.stroke}" stroke-width="1.5"/>
    <text x="${cx}" y="${BY + 27}" text-anchor="middle"
          font-family="ui-monospace, Menlo, monospace" font-size="16"
          font-weight="700" fill="${C.text}">${b.label}</text>
    <text x="${cx}" y="${BY + 46}" text-anchor="middle"
          font-family="ui-monospace, Menlo, monospace" font-size="11"
          fill="${C.muted}">${b.sub}</text>
  </g>`;
}

function arrow(fromX, toX) {
  const y = BY + BH / 2;
  return `
  <line x1="${fromX}" y1="${y}" x2="${toX - 9}" y2="${y}"
        stroke="${C.accent}" stroke-width="2" marker-end="url(#a)"/>`;
}

const arrows = boxes
  .slice(0, -1)
  .map((b, i) => arrow(b.x + BW, boxes[i + 1].x))
  .join("");

const svg = `<svg xmlns="http://www.w3.org/2000/svg" width="${W}" height="${H}" viewBox="0 0 ${W} ${H}" role="img" aria-label="Aesthetic Computer boot pipeline">
  <defs>
    <marker id="a" viewBox="0 0 10 10" refX="8" refY="5" markerWidth="7" markerHeight="7" orient="auto-start-reverse">
      <path d="M 0 0 L 10 5 L 0 10 z" fill="${C.accent}"/>
    </marker>
  </defs>
  <rect width="${W}" height="${H}" fill="${C.bg}"/>
  <text x="24" y="44" font-family="ui-monospace, Menlo, monospace" font-size="20"
        font-weight="700" fill="${C.accent}">aesthetic.computer — boot pipeline</text>
  <text x="24" y="66" font-family="ui-monospace, Menlo, monospace" font-size="12"
        fill="${C.muted}">module-loader.mjs hot-reloads pieces over WebSocket in dev</text>
  ${arrows}
  ${boxes.map(box).join("")}
  <text x="${W / 2}" y="208" text-anchor="middle"
        font-family="ui-monospace, Menlo, monospace" font-size="12" fill="${C.muted}">
    URL-addressable: aesthetic.computer/piece-name:p1:p2 · @handle/piece-name · share → QR
  </text>
  <text x="${W - 24}" y="240" text-anchor="end"
        font-family="ui-monospace, Menlo, monospace" font-size="10" fill="${C.edge}">
    generated by gen-architecture-svg.mjs — do not hand-edit
  </text>
</svg>`;

process.stdout.write(svg + "\n");
