void font_init(void) {
    // Font data is static, nothing to init
}

int font_draw(ACGraph *g, const char *text, int x, int y, int scale) {
    if (!text) return x;
    if (scale < 1) scale = 1;

    int start_x = x;
    for (const char *p = text; *p; p++) {
        unsigned char ch = (unsigned char)*p;
        if (ch == '\n') {
            x = start_x;
            y += FONT_CHAR_H * scale;
            continue;
        }
        if (ch < 32 || ch > 126) ch = '?';
        int idx = ch - 32;
        const unsigned char *glyph = font_data[idx];

        for (int row = 0; row < FONT_CHAR_H; row++) {
            unsigned char bits = glyph[row];
            for (int col = 0; col < FONT_CHAR_W; col++) {
                if (bits & (0x80 >> col)) {
                    // Draw scaled pixel
                    for (int sy = 0; sy < scale; sy++) {
                        for (int sx = 0; sx < scale; sx++) {
                            graph_plot(g, x + col * scale + sx, y + row * scale + sy);
                        }
                    }
                }
            }
        }
        x += FONT_CHAR_W * scale;
    }
    return x;
}

int font_measure(const char *text, int scale) {
    if (!text) return 0;
    if (scale < 1) scale = 1;
    int len = 0;
    for (const char *p = text; *p && *p != '\n'; p++)
        len++;
    return len * FONT_CHAR_W * scale;
}

// MatrixChunky8 BDF font rendering (variable-width, 8px tall)
int font_draw_matrix(ACGraph *g, const char *text, int x, int y, int scale) {
    if (!text) return x;
    if (scale < 1) scale = 1;

    int start_x = x;
    for (const char *p = text; *p; p++) {
        unsigned char ch = (unsigned char)*p;
        if (ch == '\n') {
            x = start_x;
            y += matrix_chunky8_ascent * scale;
            continue;
        }
        if (ch < 32 || ch > 126) ch = '?';
        const BDFGlyph *gl = &matrix_chunky8_glyphs[ch - 32];

        // Render glyph: yoff is distance from baseline to bottom of glyph
        int gy = y + (matrix_chunky8_ascent - gl->yoff - gl->height) * scale;
        int gx = x + gl->xoff * scale;

        for (int row = 0; row < gl->height && row < MATRIX_CHUNKY8_MAX_H; row++) {
            uint8_t bits = gl->rows[row];
            for (int col = 0; col < gl->width; col++) {
                if (bits & (0x80 >> col)) {
                    for (int sy = 0; sy < scale; sy++)
                        for (int sx = 0; sx < scale; sx++)
                            graph_plot(g, gx + col * scale + sx, gy + row * scale + sy);
                }
            }
        }
        x += gl->dwidth * scale;
    }
    return x;
}

int font_measure_matrix(const char *text, int scale) {
    if (!text) return 0;
    if (scale < 1) scale = 1;
    int w = 0;
    for (const char *p = text; *p && *p != '\n'; p++) {
        unsigned char ch = (unsigned char)*p;
        if (ch < 32 || ch > 126) ch = '?';
        w += matrix_chunky8_glyphs[ch - 32].dwidth;
    }
    return w * scale;
}

// 6x10 fixed-width BDF font rendering
int font_draw_6x10(ACGraph *g, const char *text, int x, int y, int scale) {
    if (!text) return x;
    if (scale < 1) scale = 1;

    int start_x = x;
    const char *p = text;
    while (*p) {
        if (*p == '\n') {
            x = start_x;
            y += FONT_6X10_H * scale;
            p++;
            continue;
        }
        int cp = utf8_next(&p);
        if (cp < 0) continue;
        if (cp >= 32 && cp <= 126) {
            // ASCII — render from the 6x10 bitmap
            int idx = cp - 32;
            const uint8_t *glyph = font_6x10_data[idx];
            for (int row = 0; row < FONT_6X10_H; row++) {
                uint8_t bits = glyph[row];
                for (int col = 0; col < FONT_6X10_W; col++) {
                    if (bits & (0x80 >> col)) {
                        for (int sy = 0; sy < scale; sy++)
                            for (int sx = 0; sx < scale; sx++)
                                graph_plot(g, x + col * scale + sx, y + row * scale + sy);
                    }
                }
            }
            x += FONT_6X10_W * scale;
        } else if (cp > 126) {
            // Non-ASCII — try Unicode fallback (unifont subset)
            const UnicodeGlyph *u = unicode_lookup((uint32_t)cp);
            if (u) {
                draw_unicode_glyph(g, u, x, y, scale);
                x += FONT_6X10_W * scale; // Keep monospace grid
            } else {
                // Missing glyph — draw ? as a placeholder
                const uint8_t *glyph = font_6x10_data['?' - 32];
                for (int row = 0; row < FONT_6X10_H; row++) {
                    uint8_t bits = glyph[row];
                    for (int col = 0; col < FONT_6X10_W; col++) {
                        if (bits & (0x80 >> col)) {
                            for (int sy = 0; sy < scale; sy++)
                                for (int sx = 0; sx < scale; sx++)
                                    graph_plot(g, x + col * scale + sx, y + row * scale + sy);
                        }
                    }
                }
                x += FONT_6X10_W * scale;
            }
        } else {
            // Control chars — skip silently
        }
    }
    return x;
}

int font_measure_6x10(const char *text, int scale) {
    if (!text) return 0;
    if (scale < 1) scale = 1;
    int len = 0;
    const char *p = text;
    while (*p && *p != '\n') {
        int cp = utf8_next(&p);
        if (cp > 0) len++;
    }
    return len * FONT_6X10_W * scale;
}
