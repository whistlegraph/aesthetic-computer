int font_draw(ACGraph *g, const char *text, int x, int y, int scale) {
    if (!text) return x;
    if (scale < 1) scale = 1;

    int start_x = x;
    for (const char *p = text; *p; p++) {
        unsigned char ch = (unsigned char)*p;
        if (ch == '\n') {
            x = start_x;
            y += FONT_CHAR_H * scale;
            continue;
        }
        if (ch < 32 || ch > 126) ch = '?';
        int idx = ch - 32;
        const unsigned char *glyph = font_data[idx];

        for (int row = 0; row < FONT_CHAR_H; row++) {
            unsigned char bits = glyph[row];
            for (int col = 0; col < FONT_CHAR_W; col++) {
                if (bits & (0x80 >> col)) {
                    // Draw scaled pixel
                    for (int sy = 0; sy < scale; sy++) {
                        for (int sx = 0; sx < scale; sx++) {
                            graph_plot(g, x + col * scale + sx, y + row * scale + sy);
                        }
                    }
                }
            }
        }
        x += FONT_CHAR_W * scale;
    }
    return x;
}

int font_measure(const char *text, int scale) {
    if (!text) return 0;
    if (scale < 1) scale = 1;
    int len = 0;
    for (const char *p = text; *p && *p != '\n'; p++)
        len++;
    return len * FONT_CHAR_W * scale;
}
